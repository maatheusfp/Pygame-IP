class Level1():
    def __init__(self):
        self.posicoesValidas = list()

        linhaCoord = 0
        for linha in enumerate(layout):
            valorCoord = 0
            for valor in linha:
                if valor == 'X':
                    x = valorCoord * tyle[0]
                    y = linhaCoord * tyle[1]
                    self.posicoesValidas.append(x,y)
                    
                valorCoord += 1

            linhaCoord += 1

